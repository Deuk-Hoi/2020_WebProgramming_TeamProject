package com.webprogramming.project;

public class OrderDO {
	String pickup=null;
	String seatnum=null;
	String cafetime=null;
	String takeouttime=null;
	String ab=null;
	String price=null;
	
	
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getAb() {
		return ab;
	}
	public void setAb(String ab) {
		this.ab = ab;
	}
	public String getTakeouttime() {
		return takeouttime;
	}
	public void setTakeouttime(String takeouttime) {
		this.takeouttime = takeouttime;
	}
	public String getSeatnum() {
		return seatnum;
	}
	public void setSeatnum(String seatnum) {
		this.seatnum = seatnum;
	}
	public String getCafetime() {
		return cafetime;
	}
	public void setCafetime(String cafetime) {
		this.cafetime = cafetime;
	}
	public String getPickup() {
		return pickup;
	}
	public void setPickup(String pickup) {
		this.pickup = pickup;
	}
	
	 
}
